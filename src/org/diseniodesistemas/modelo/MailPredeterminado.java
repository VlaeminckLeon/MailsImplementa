package org.diseniodesistemas.modelo;

public class MailPredeterminado {

	private String mailPredeterminadoCode;
	
	public MailPredeterminado(String mailPredeterminadoCode
			) {
		super();
		this.setMailPredeterminadoCode(mailPredeterminadoCode);
	}

	public String getMailPredeterminadoCode() {
		return mailPredeterminadoCode;
	}

	public void setMailPredeterminadoCode(String mailPredeterminadoCode) {
		this.mailPredeterminadoCode = mailPredeterminadoCode;
	}
		
}
